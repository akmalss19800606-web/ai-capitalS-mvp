# Попытка декодировать двойную кодировку
# Текст закодирован как UTF-8 -> интерпретирован как Latin-1 -> закодирован обратно в UTF-8
def fix_double_utf8(s):
    """Пытается исправить двойное кодирование UTF-8"""
    try:
        return s.encode('latin-1').decode('utf-8')
    except:
        return s

# Находим все строки в кавычках и пытаемся исправить
def fix_quoted_strings(match):
    original = match.group(0)
    inner = match.group(1)
    fixed = fix_double_utf8(inner)
    if fixed != inner:
        return '"' + fixed + '"'
    return original

